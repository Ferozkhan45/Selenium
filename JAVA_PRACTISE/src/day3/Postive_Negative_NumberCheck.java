package day3;

public class Postive_Negative_NumberCheck {

	public static void main(String[] args) 
	{
	
		int num = 9876;
		
		if(num>0)
		{
			System.out.println("Positive Number");
		}
		else if(num<0)
		{
			System.out.println("Negative Number");
		}
		else
		{
			System.out.println("0");
		}
	}

}
